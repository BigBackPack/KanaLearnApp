let currentPick;

function pickKana(x){
    let randomPick = Math.floor(Math.random() * x.length);
    let newKana = x[randomPick];
    var newSolution = solutionKatakana[randomPick];
    currentPick = newSolution;

    document.getElementById("kanaContainer").innerHTML = `${newKana}`;
    document.getElementById("solution").innerHTML = `${newSolution}`;
}

function showSolution(){
    document.getElementById("solution").style.display = 'block';
    checkSolution()
}


function checkSolution(){
let userInput = document.getElementById("userInput").value.toLowerCase();

    if(userInput === currentPick){
        document.getElementById("eval").innerHTML = "correct!"
    } else {
        document.getElementById("eval").innerHTML = "wrong!"
    }
}






